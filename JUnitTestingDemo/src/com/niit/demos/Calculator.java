package com.niit.demos;

public class Calculator {
	
	public int add(int num1, int num2) {
		return num1+num2;
	}
	
	public int subract(int num1, int num2) {
		return num1-num2;
	}
	
	public int multiple(int num1, int num2) {
		return num1*num2;
	}
	
	public int divide(int num1, int num2) {
		return num1/num2;
	}
	
	
	
	

}
